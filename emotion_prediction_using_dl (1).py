import nltk
nltk.download('stopwords')

from nltk.corpus import stopwords
stopwords.words('english')

def remove_stopwords(text):
  x=[]
  for i in text.split():
    if i not in stopwords.words('english'):
      x.append(i)
  y=x[:]
  x.clear()
  return y

df['text']=df['text'].apply(remove_stopwords)

print (df.head())

y=[]
from nltk.stem import PorterStemmer
ps=PorterStemmer()
def stem_words(text):
  for i in text:
    y.append(ps.stem(i))
  z=y[:]
  y.clear()
  return z

df['text']=df['text'].apply(stem_words)

df.head()

def join_back(list_input):
  return " ".join(list_input)

df['text']=df['text'].apply(join_back)

df.head()

from sklearn.feature_extraction.text import CountVectorizer
cv=CountVectorizer(max_features=150)

X=cv.fit_transform(df['text']).toarray()

X[1:5,:]

X.shape

y=df.iloc[:,-1].values

from sklearn.model_selection import train_test_split
X_train,X_test,Y_train,Y_test=train_test_split(X,y,test_size=0.2)

model=Sequential()

model.add(Conv1D(filters=32,kernel_size=2, activation='relu',input_shape=(X_train.shape[1],1)))

model.add(MaxPooling1D(pool_size=2))

model.add(Flatten())

from tensorflow.keras.layers import Dense
model.add(Dense(16,activation='relu'))

model.add(Dense(6,activation='softmax'))

model.compile(optimizer='adam',loss='categorical_crossentropy',metrics=['accuracy'])

from tensorflow.keras.utils import to_categorical

# One-hot encode the target variable
Y_train_encoded = to_categorical(Y_train, num_classes=6)
Y_test_encoded = to_categorical(Y_test, num_classes=6)

model.fit(X_train, Y_train_encoded, epochs=800, batch_size=16)

loss,accuracy=model.evaluate(X_test,Y_test_encoded)
print("Accuracy",accuracy)